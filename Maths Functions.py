# MATHS FUNCTIONS

# the function pow(x, y) returns the value x to the power of y
x = pow(4, 3) # Returns 4 to the power of 3
print(x)

import math #SOme maths functio require you to import math

# the math.sqrt returns the square root of a number
y = math.sqrt(64)
print(y)

# math.ceil rounds the number up to the nearest integer while math.floor rounds
# the number down to the nearest integer

a = math.ceil(4.8)# rounds the number up
b = math.floor(3.7)# rounds the number down
print(a)
print(b)

# math.pi returns the value of pi
c = math.pi
print(c)

